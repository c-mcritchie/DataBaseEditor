package ca.sait.models;

import java.io.Serializable;

/**
 *
 * @author colem
 */
public class Role implements Serializable {
    private String name;
    private int id;
    
    public Role() {
        
    }

    public Role(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
}
